package interfaz;

/**
 * Interfaz que contiene dos metodos de encendido y apagado
 * @author David
 */
public interface IVehiculo {
    
    /**
     * metodo que dice si el vehiculo esta prendido 
     */
    public void encender();
    /**
     * metodo que dice si el vehiculo esta apagado
     */
    public void apagar();
}
